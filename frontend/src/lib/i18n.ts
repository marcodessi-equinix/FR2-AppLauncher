export type TranslationKey = keyof typeof translations.de;

const interpolate = (template: string, params?: TranslationParams) => {
  if (!params) {
    return template;
  }

  return Object.entries(params).reduce((result, [key, value]) => {
    return result.split(`{${key}}`).join(String(value));
  }, template);
};

export const getLocale = (language: PreferredLanguage) => LOCALES[language];

export const translate = (
  language: PreferredLanguage,
  key: TranslationKey,
  params?: TranslationParams,
) => {
  const value = translations[language][key] ?? translations.de[key] ?? key;
  return interpolate(value, params);
};

const normalizeDate = (value: Date | string | number) =>
  value instanceof Date ? value : new Date(value);

export const useI18n = () => {
  const language = useStore((state) => state.preferredLanguage);
  const setLanguage = useStore((state) => state.setPreferredLanguage);

  const locale = React.useMemo(() => getLocale(language), [language]);

  const t = React.useCallback(
    (key: TranslationKey, params?: TranslationParams) => translate(language, key, params),
    [language],
  );

  const toggleLanguage = React.useCallback(() => {
    setLanguage(language === 'de' ? 'en' : 'de');
  }, [language, setLanguage]);

  const formatDate = React.useCallback(
    (value: Date | string | number, options?: Intl.DateTimeFormatOptions) =>
      new Intl.DateTimeFormat(locale, options).format(normalizeDate(value)),
    [locale],
  );

  const formatTime = React.useCallback(
    (value: Date | string | number, options?: Intl.DateTimeFormatOptions) =>
      new Intl.DateTimeFormat(locale, options).format(normalizeDate(value)),
    [locale],
  );

  return {
    language,
    locale,
    setLanguage,
    toggleLanguage,
    t,
    formatDate,
    formatTime,
  };
};